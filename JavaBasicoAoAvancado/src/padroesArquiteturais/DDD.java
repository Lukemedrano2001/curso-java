public class DDD {
	/*
		Domain-Drive-Design:
			- Domain: 
				1-) Responsável pelas regras de negócio e validação da aplicação. 
				2-) Contém as entidades, agregados, serviços e objetos de valor. 
				3-) Foco na modelagem do conhecimento especializado da aplicação.
			
			- Repository: 
				1-) Responsável por fornecer uma interface para acessar e gerenciar os objetos do domínio. 
				2-) Abstrai a camada de acesso a dados, permitindo que a lógica da aplicação se concentre nas regras de negócio. 
				3-) Facilita a recuperação e armazenamento de objetos do domínio sem se preocupar com detalhes de persistência.
			
			- Infraestrutura: 
				1-) Abrange todos os aspectos técnicos e tecnológicos necessários para dar suporte à aplicação, mas que não fazem parte do núcleo da lógica de domínio. 
				2-) Isso inclui bancos de dados, frameworks, serviços externos, camadas de persistência, entre outros
				3-) Permite que a lógica de domínio permaneça independente dos detalhes de implementação técnica.
	*/
}